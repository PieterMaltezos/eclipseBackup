package be.pxl.h5.opdr6;

public class H5Opdr6 {

	public static void main(String[] args) {
		
		int a = 8;
		int b = -6;
		System.out.println("a: " + a);
		System.out.println("b: " + b);
		System.out.println("a > b    : " + (a > b));
		System.out.println("a >= b   : " + (a >= b));
		System.out.println("a < b    : " + (a < b));
		System.out.println("a <= b   : " + (a <= b));
		System.out.println("a == b   : " + (a == b));
		System.out.println("a != b   : " + (a != b));
	}

}
